package com.gerador.models.dtos.agency;

public class AgencyDTO {
	private String id;
	private String label;
	
	public AgencyDTO() {
		super();
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}
	
}
