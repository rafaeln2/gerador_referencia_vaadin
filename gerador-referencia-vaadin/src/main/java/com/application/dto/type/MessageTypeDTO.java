package com.application.dto.type;

public class MessageTypeDTO {
	private String id;
	private String label;
	
	public MessageTypeDTO() {
		super();
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}
}
